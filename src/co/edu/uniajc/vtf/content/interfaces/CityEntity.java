package co.edu.uniajc.vtf.content.interfaces;

public class CityEntity {
	private int Id;
	private String Name;
	private int WOEId;
	
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getWOEId() {
		return WOEId;
	}
	public void setWOEId(int wOEId) {
		WOEId = wOEId;
	}
	
	
}
